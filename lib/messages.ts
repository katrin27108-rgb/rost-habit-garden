export const SUPPORT_MESSAGES = beginnings.flatMap((beginning, row) => endings.map((ending, column) => ({ code: `warm-${row}-${column}`, text: `${beginning} ${ending}` })));

export function messageForDay(day: DateKey, recentlyShown: string[] = []) {
  const epochDay = Math.floor(new Date(`${day}T12:00:00Z`).getTime() / 86_400_000);
  for (let offset = 0; offset < SUPPORT_MESSAGES.length; offset += 1) {
    const message = SUPPORT_MESSAGES[(epochDay + offset * 37) % SUPPORT_MESSAGES.length];
    if (!recentlyShown.slice(-30).includes(message.code)) return message;
  }
  return SUPPORT_MESSAGES[epochDay % SUPPORT_MESSAGES.length];
}

